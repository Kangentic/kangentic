/**
 * Unit tests for TASK_BULK_DELETE IPC handler.
 *
 * The handler body is extracted by capturing the function registered with
 * ipcMain.handle for IPC.TASK_BULK_DELETE and invoking it directly. All
 * dependencies (fs, getProjectRepos, withTaskLock, cleanupTaskResources,
 * PQueue, ipcMain, electron) are mocked so no Electron binary or real
 * filesystem is needed.
 *
 * Covers:
 *   - empty ids array early return (no progress events, deleted=0, failures=[])
 *   - happy path: all tasks deleted cleanly, deleted=N, failures=[]
 *   - orphan detection: cleanupTaskResources succeeds but directory survives
 *     on disk -> failure pushed with "Worktree directory could not be removed"
 *   - withTaskLock throw path: error caught, pushed to failures, no re-throw
 *   - progress events emitted per task including the initial emit
 */

