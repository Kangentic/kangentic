describe('TASK_CREATE handler', () => {
  let context: MockContext;
  let task: MockTask;
  let taskRepo: MockTaskRepo;
  let attachmentRepo: MockAttachmentRepo;
  let swimlaneRepo: MockSwimlaneRepo;
  let targetLane: MockSwimlane;

  beforeEach(() => {
    vi.clearAllMocks();
    capturedHandlers.clear();

    targetLane = createMockSwimlane('lane-doing', { auto_spawn: true });
    task = createMockTask('task-new', { swimlane_id: 'lane-doing' });
    taskRepo = createMockTaskRepo(task);
    attachmentRepo = createMockAttachmentRepo();
    swimlaneRepo = createMockSwimlaneRepo([targetLane]);
    context = createMockContext();

    mockGetProjectRepos.mockReturnValue({
      tasks: taskRepo,
      swimlanes: swimlaneRepo,
      actions: { getTransitionsFor: vi.fn(() => []) },
      attachments: attachmentRepo,
    });

    mockEnsureTaskWorktree.mockResolvedValue(null);
    mockEnsureTaskBranchCheckout.mockResolvedValue(undefined);

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    registerTaskCrudHandlers(context as never);
  });

  // =========================================================================
  // auto_spawn=false: skips entire lock block
  // =========================================================================

  it('skips worktree, transition, and auto-command when target lane has auto_spawn=false', async () => {
    const noSpawnLane = createMockSwimlane('lane-backlog', { auto_spawn: false });
    swimlaneRepo = createMockSwimlaneRepo([noSpawnLane]);
    const noSpawnTask = createMockTask('task-no-spawn', { swimlane_id: 'lane-backlog' });
    taskRepo = createMockTaskRepo(noSpawnTask);

    mockGetProjectRepos.mockReturnValue({
      tasks: taskRepo,
      swimlanes: swimlaneRepo,
      actions: { getTransitionsFor: vi.fn(() => []) },
      attachments: attachmentRepo,
    });

    const result = await callCreateHandler(context, {
      swimlane_id: 'lane-backlog',
      title: 'No spawn task',
    });

    expect(result).toMatchObject({ id: 'task-no-spawn' });
    expect(mockEnsureTaskWorktree).not.toHaveBeenCalled();
    expect(mockCreateTransitionEngine).not.toHaveBeenCalled();
    expect(context.commandInjector.schedule).not.toHaveBeenCalled();
  });

  it('skips lock block when currentProjectPath is null', async () => {
    context.currentProjectPath = null;
    capturedHandlers.clear();
    registerTaskCrudHandlers(context as never);

    const result = await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'No path task',
    });

    expect(result).toMatchObject({ id: 'task-new' });
    expect(mockEnsureTaskWorktree).not.toHaveBeenCalled();
    expect(mockCreateTransitionEngine).not.toHaveBeenCalled();
  });

  it('skips lock block when currentProjectId is null', async () => {
    context.currentProjectId = null;
    capturedHandlers.clear();
    registerTaskCrudHandlers(context as never);

    const result = await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'No project task',
    });

    expect(result).toMatchObject({ id: 'task-new' });
    expect(mockEnsureTaskWorktree).not.toHaveBeenCalled();
  });

  // =========================================================================
  // Worktree error branch
  // =========================================================================

  it('returns task without spawning when ensureTaskWorktree throws (lock released cleanly)', async () => {
    mockEnsureTaskWorktree.mockRejectedValue(new Error('git worktree add failed'));

    const result = await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Worktree error task',
    });

    expect(result).toMatchObject({ id: 'task-new' });
    expect(mockCreateTransitionEngine).not.toHaveBeenCalled();
    expect(context.commandInjector.schedule).not.toHaveBeenCalled();
  });

  // =========================================================================
  // Checkout error branch
  // =========================================================================

  it('returns task without spawning when ensureTaskBranchCheckout throws', async () => {
    mockEnsureTaskBranchCheckout.mockRejectedValue(new Error('branch locked by active session'));

    const result = await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Checkout error task',
    });

    expect(result).toMatchObject({ id: 'task-new' });
    expect(mockCreateTransitionEngine).not.toHaveBeenCalled();
    expect(context.commandInjector.schedule).not.toHaveBeenCalled();
  });

  // =========================================================================
  // Transition engine called with '*' wildcard
  // =========================================================================

  it('calls executeTransition with "*" wildcard as fromSwimlaneId', async () => {
    const taskWithSession = { ...task, session_id: 'session-abc' };
    taskRepo.getById.mockReturnValue(taskWithSession);

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Wildcard transition task',
    });

    expect(engine.executeTransition).toHaveBeenCalledWith(
      expect.objectContaining({ id: 'task-new' }),
      '*',
      'lane-doing',
      targetLane.permission_mode,
    );
  });

  // =========================================================================
  // resumeSuspendedSession fallback
  // =========================================================================

  it('calls resumeSuspendedSession when task has no session_id after executeTransition', async () => {
    // getById returns task with no session_id (transition did not set one)
    taskRepo.getById.mockReturnValue({ ...task, session_id: null });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Resume fallback task',
    });

    expect(engine.resumeSuspendedSession).toHaveBeenCalledWith(
      expect.objectContaining({ id: 'task-new', session_id: null }),
      targetLane.permission_mode,
    );
  });

  it('does NOT call resumeSuspendedSession when task already has session_id after transition', async () => {
    // Simulate: transition engine set the session_id
    taskRepo.getById.mockReturnValue({ ...task, session_id: 'session-from-transition' });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'No resume task',
    });

    expect(engine.resumeSuspendedSession).not.toHaveBeenCalled();
  });

  // =========================================================================
  // Auto-command scheduling
  // =========================================================================

  it('schedules auto-command when session exists and lane has auto_command', async () => {
    const autoCommandLane = createMockSwimlane('lane-auto', {
      auto_spawn: true,
      auto_command: '/run-tests',
    });
    swimlaneRepo = createMockSwimlaneRepo([autoCommandLane]);
    const autoTask = createMockTask('task-auto', { swimlane_id: 'lane-auto' });
    taskRepo = createMockTaskRepo(autoTask);
    taskRepo.getById.mockReturnValue({ ...autoTask, session_id: 'session-auto' });

    mockGetProjectRepos.mockReturnValue({
      tasks: taskRepo,
      swimlanes: swimlaneRepo,
      actions: { getTransitionsFor: vi.fn(() => []) },
      attachments: attachmentRepo,
    });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-auto',
      title: 'Auto command task',
    });

    expect(context.commandInjector.schedule).toHaveBeenCalledWith(
      'task-auto',
      'session-auto',
      '/run-tests',
      { freshlySpawned: true },
    );
  });

  it('does NOT schedule auto-command when finalTask has no session_id', async () => {
    const autoCommandLane = createMockSwimlane('lane-auto-nosession', {
      auto_spawn: true,
      auto_command: '/run-tests',
    });
    swimlaneRepo = createMockSwimlaneRepo([autoCommandLane]);
    const autoTask = createMockTask('task-no-session-auto', { swimlane_id: 'lane-auto-nosession' });
    taskRepo = createMockTaskRepo(autoTask);
    // Even after resumeSuspendedSession, getById returns task with no session_id
    taskRepo.getById.mockReturnValue({ ...autoTask, session_id: null });

    mockGetProjectRepos.mockReturnValue({
      tasks: taskRepo,
      swimlanes: swimlaneRepo,
      actions: { getTransitionsFor: vi.fn(() => []) },
      attachments: attachmentRepo,
    });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-auto-nosession',
      title: 'No session auto task',
    });

    expect(context.commandInjector.schedule).not.toHaveBeenCalled();
  });

  it('does NOT schedule auto-command when lane has no auto_command', async () => {
    taskRepo.getById.mockReturnValue({ ...task, session_id: 'session-no-cmd' });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'No auto command task',
    });

    // targetLane.auto_command is null
    expect(context.commandInjector.schedule).not.toHaveBeenCalled();
  });

  // =========================================================================
  // Pending attachments
  // =========================================================================

  it('saves pending attachments when provided', async () => {
    const pendingAttachments = [
      { filename: 'screenshot.png', data: 'base64data', media_type: 'image/png' },
    ];

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Task with attachment',
      pendingAttachments,
    });

    expect(attachmentRepo.add).toHaveBeenCalledWith(
      '/mock/project',
      'task-new',
      'screenshot.png',
      'base64data',
      'image/png',
    );
  });

  it('does NOT save attachments when pendingAttachments is empty', async () => {
    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Task no attachments',
      pendingAttachments: [],
    });

    expect(attachmentRepo.add).not.toHaveBeenCalled();
  });

  it('does NOT save attachments when currentProjectPath is null', async () => {
    context.currentProjectPath = null;
    capturedHandlers.clear();
    registerTaskCrudHandlers(context as never);

    const pendingAttachments = [
      { filename: 'photo.jpg', data: 'data', media_type: 'image/jpeg' },
    ];

    await callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Task no project path',
      pendingAttachments,
    });

    expect(attachmentRepo.add).not.toHaveBeenCalled();
  });

  // =========================================================================
  // withTaskLock engagement: concurrent TASK_CREATE for the same task id
  // =========================================================================

  it('serializes concurrent withTaskLock calls for the same task id', async () => {
    // Use the hold pattern from task-lifecycle-lock.test.ts:
    // first invocation holds until released; second must wait.
    let releaseFirst!: () => void;
    const firstHold = new Promise<void>((resolve) => {
      releaseFirst = resolve;
    });
    const entryOrder: number[] = [];

    mockEnsureTaskWorktree.mockImplementation(async () => {
      entryOrder.push(entryOrder.length);
      if (entryOrder.length === 1) {
        await firstHold;
      }
    });

    // Both invocations share the same task.id returned by tasks.create.
    // That id is what withTaskLock keys on.
    taskRepo.getById.mockReturnValue({ ...task, session_id: null });

    const engine = createMockEngine();
    mockCreateTransitionEngine.mockReturnValue(engine);

    const first = callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'First concurrent task',
    });
    const second = callCreateHandler(context, {
      swimlane_id: 'lane-doing',
      title: 'Second concurrent task',
    });

    // Only the first invocation should have entered ensureTaskWorktree yet
    await Promise.resolve();
    expect(entryOrder).toHaveLength(1);

    releaseFirst();
    await Promise.all([first, second]);

    // Both entered, in submission order
    expect(entryOrder).toHaveLength(2);
    expect(entryOrder).toEqual([0, 1]);
  });
});
